
print("Hello! you are here to calculate the area of the rectangle.")

# The user has to enter both the length and the width of a rectangle 
length = int(input("Enter the length of the rectangle: "))
breadth = int(input("Enter the width of the rectangle: "))

#The formula to use when calculating the are of a rectangle(a = l * b)
area = length * breadth

print("The area of the rectangle is: ", area)